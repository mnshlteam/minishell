#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = 0;
	while (1)
	{
		if (*s == (char)c)
			tmp = (char *)s;
		if (!*s)
			return (tmp);
		s++;
	}
}
